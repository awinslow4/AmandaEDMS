package amanda.edms;

public class FolderData {

    private String folderType;

    private String folderSection;

    private String folderRevision;

    private String folderSequence;

    private String folderYear;

    public String getFolderType() {
        return folderType;
    }

    public void setFolderType(String folderType) {
        this.folderType = folderType;
    }

    public String getFolderSection() {
        return folderSection;
    }

    public void setFolderSection(String folderSection) {
        this.folderSection = folderSection;
    }

    public String getFolderRevision() {
        return folderRevision;
    }

    public void setFolderRevision(String folderRevision) {
        this.folderRevision = folderRevision;
    }

    public String getFolderSequence() {
        return folderSequence;
    }

    public void setFolderSequence(String folderSequence) {
        this.folderSequence = folderSequence;
    }

    public String getFolderYear() {
        return folderYear;
    }

    public void setFolderYear(String folderYear) {
        this.folderYear = folderYear;
    }
}
